package com.example.formulazero.Model;

import javax.persistence.*;

@Entity
@Table(name = "users")
public class User {
    @Id
    @Column
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column
    private String email;

    @Column
    private String password;

    @Column
    private String name;

    @Column
    private String homeCountry;

    @Column
    private String favoriteTeam;

    @Column
    private String favoriteDriver;

    @ManyToOne
    @JoinColumn(name = "experience_id")
    private Experience experience;

    public User() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHomeCountry() {
        return homeCountry;
    }

    public void setHomeCountry(String homeCountry) {
        this.homeCountry = homeCountry;
    }

    public String getFavoriteTeam() {
        return favoriteTeam;
    }

    public void setFavoriteTeam(String favoriteTeam) {
        this.favoriteTeam = favoriteTeam;
    }

    public String getFavoriteDriver() {
        return favoriteDriver;
    }

    public void setFavoriteDriver(String favoriteDriver) {
        this.favoriteDriver = favoriteDriver;
    }

    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", email='" + email + '\'' +
                ", password='" + password + '\'' +
                ", name='" + name + '\'' +
                ", homeCountry='" + homeCountry + '\'' +
                ", favoriteTeam='" + favoriteTeam + '\'' +
                ", favoriteDriver='" + favoriteDriver + '\'' +
                '}';
    }
}
